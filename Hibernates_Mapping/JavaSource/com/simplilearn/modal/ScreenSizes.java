package com.simplilearn.modal;

public class ScreenSizes {

	private long screenId;
	private String size;

	public ScreenSizes() {
	}

	public ScreenSizes(String size) {
		this.size = size;
	}

	public long getScreeId() {
		return screenId;
	}

	public void setScreeId(long screeId) {
		this.screenId = screeId;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}
	
	

}
